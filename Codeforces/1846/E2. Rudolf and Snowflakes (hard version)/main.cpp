constexpr int MOD = 1000000007;
using Z = mint<int32_t>;

int power(int x, ll b) {
    int res = 1, a = x;
    for (; b; b >>= 1, a = a * a % mod) {
        if (b & 1) res = res * a % mod;
    }
    return res;
}

ll n;

__int128 calc(long long k, int x) {
    __int128 val = 0;
    __int128 cur = 1;
    for (int i = 0; val <= n && i <= x; i++) {
        val += cur;
        cur *= k;
    }
    return val;
}

void solve() {
    cin >> n;

    for (int x = 2; x <= 70; x++) {
        // binary search for k
        long long s = 1, e = 1e18;
        long long k = -1;
        while (e - s > 1) {
            long long mid = (s + e) / 2;
            auto val = calc(mid, x);

            if (val >= n) {
                k = e = mid;
            } else {
                s = mid;
            }
        }


        if (k == -1) continue;
        if (calc(k, x) == n) {
            cout << "YES\n";
            return;
        }
    }

    cout << "NO\n";
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL), cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
