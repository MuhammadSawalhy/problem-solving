template<class T>
using rpq = priority_queue<T, vector<T>, greater<T>>;

void solve() {
    int n, k;
    cin >> n >> k;
    if (n == k) {
        for (int i = 0; i < n; i++) cout << "1 ";
    } else if (k == 1) {
        for (int i = 0; i < n; i++) cout << i + 1 << ' ';
    } else {
        cout << "-1";
    }

    cout << endl;
}

int32_t main(int32_t argc, char **argv) {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL), cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
