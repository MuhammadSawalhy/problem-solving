#define all(v) v.begin(), v.end()

void solve() {
    int n, m;
    cin >> n >> m;

    vector<int> adj[n];
    for (int i = 0; i < m; i++) {
        int u, v;
        cin >> u >> v;
        u--, v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    for (int i = 0; i < n; i++) {
        if (adj[i].size() == 1) {
            int y = adj[adj[i][0]].size() - 1;
            for (auto j: adj[adj[i][0]]) {
                if (adj[j].size() != 1) {
                    cout << adj[j].size() << ' ' << y << endl;
                    return;
                }
            }
        }
    }
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL), cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
