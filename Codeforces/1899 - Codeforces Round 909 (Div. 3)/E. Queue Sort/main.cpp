#define rall(v) v.rbegin(), v.rend()
#define minit(v, x) v = min(v, x)
#define maxit(v, x) v = max(v, x)

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    int i = n - 1;
    while (i > 0 && a[i - 1] <= a[i]) {
        i--;
    }

    for (int j = 0; j < i; j++) {
        if (a[i] >= a[j]) {
            cout << -1 << '\n';
            return;
        }
    }

    cout << i << '\n';
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL), cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
