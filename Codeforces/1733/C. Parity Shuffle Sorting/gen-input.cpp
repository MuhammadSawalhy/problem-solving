#include <bits/stdc++.h>

using namespace std;

int main(int argc, char *argv[]) {
  int t = pow(10, 5);
  cout << t << endl;
  for (int i = 0; i < 10; i++) {
    for (int j = 0; j < 10; j++) {
      for (int k = 0; k < 10; k++) {
        for (int l = 0; l < 10; l++) {
          for (int m = 0; m < 10; m++) {
            cout << 5 << endl;
            cout << i << " " << j << " " << k << " " << l << " " << m << endl;
          }
        }
      }
    }
  }
  return 0;
}
