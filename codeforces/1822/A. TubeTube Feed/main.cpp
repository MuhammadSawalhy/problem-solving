void solve() {
    int n, t;
    cin >> n >> t;
    int a[n], b[n];
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }

    int mx = 0, ind = -2;
    for (int i = 0; i < n; i++) {
        if (b[i] > mx && i + a[i] <= t) {
            mx = b[i];
            ind = i;
        }
    }

    cout << ind + 1 << "\n";
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL), cout.tie(NULL);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
